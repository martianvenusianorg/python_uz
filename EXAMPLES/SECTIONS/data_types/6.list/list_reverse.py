# List.reverse() function
ages = [27, 28, 18, 19]
print(ages)
# [19, 18, 28, 27]

ages = [27, 28, 18, 19]
ages.reverse()
print(ages)
# [19, 18, 28, 27]

names = ['akbar', 'akrom', 'huoyiddin', 'dilmurod']
print(names)
# ['akbar', 'akrom', 'huoyiddin', 'dilmurod']

names.reverse()
print(names)
# ['dilmurod', 'huoyiddin', 'akrom', 'akbar']
